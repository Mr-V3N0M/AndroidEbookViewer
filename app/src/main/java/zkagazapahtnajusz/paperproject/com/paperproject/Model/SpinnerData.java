package zkagazapahtnajusz.paperproject.com.paperproject.Model;

public class SpinnerData {
    String text;
    Integer image;

    public SpinnerData(String text, Integer image) {
        this.text = text;
        this.image = image;
    }

    public String getText() {
        return text;
    }

    public Integer getImage() {
        return image;
    }
}
